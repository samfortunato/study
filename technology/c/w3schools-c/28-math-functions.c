#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main() {
	printf("%lf\n", sqrt(4));
	printf("%lf\n", cbrt(9));

	printf("%f\n", floor(1.1));
	printf("%f\n", ceil(1.8));

	printf("%d\n", abs(-1));

	printf("%lf\n", pow(2, 2));
	printf("%lf\n", exp(2));

	printf("%lf\n", acos(1));
	printf("%lf\n", asin(1));
	printf("%lf\n", atan(1));
	printf("%lf\n", cos(1));
	printf("%lf\n", sin(1));
	printf("%lf\n", tan(1));

	return 0;
}
